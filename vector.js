class Vec2
{
    constructor(x, y)
    {
        this.x = x;
        this.y = y;
    }
}

class Vec3
{
    constructor(x, y, z)
    {
        this.x = x;
        this.y = y;
        this.z = z;
    }
}

class Face
{
    constructor(a, b, c, color)
    {
        this.a = a;
        this.b = b;
        this.c = c;
        this.color = color;
    }
}